package dk.fust.docgen.excel.format.table.model;

/**
 * Key for looking up difference styles for the cells in the sheet
 */
public enum CellStyleId {

    /**
     * Used for headers
     */
    HEADER,

    /**
     * Used for secondary headers
     */
    SECONDARY_HEADER,

    /**
     * Style used for even rows
     */
    EVEN_ROW,

    /**
     * Style used for odd rows
     */
    ODD_ROW

}
